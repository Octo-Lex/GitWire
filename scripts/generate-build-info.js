#!/usr/bin/env node
/**
 * scripts/generate-build-info.js
 *
 * Reads root package.json for version, derives git SHA, and overwrites the
 * committed fallback build-info modules with real values.
 *
 * Used during Docker/CI builds. Safe to run locally but not required —
 * the committed fallback modules carry the current release version.
 *
 * Outputs:
 *   packages/core/src/buildInfo.js       (backend build-info)
 *
 * The dashboard module (packages/web-dashboard/src/lib/buildInfo.ts) is
 * env-aware and does NOT need to be overwritten by this script — the
 * Dockerfile passes NEXT_PUBLIC_GITWIRE_* build args directly.
 */

import { readFile, writeFile } from "node:fs/promises";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { execSync } from "node:child_process";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

// ── Read version from root package.json ─────────────────────────────────────
const pkgRaw = await readFile(join(ROOT, "package.json"), "utf-8");
const pkg = JSON.parse(pkgRaw);
const version = pkg.version;

// ── Derive git SHA (short, 12 chars) ───────────────────────────────────────
let gitSha = process.env.GITWIRE_COMMIT_SHA || null;
if (!gitSha) {
  try {
    gitSha = execSync("git rev-parse --short=12 HEAD", { cwd: ROOT, encoding: "utf-8" }).trim();
  } catch {
    gitSha = null; // Not a git repo or git unavailable
  }
}

const builtAt = new Date().toISOString();
const source = "generated";

console.log(`[build-info] version=${version} git_sha=${gitSha || "null"} built_at=${builtAt}`);

// ── Write backend build-info ────────────────────────────────────────────────
const corePath = join(ROOT, "packages", "core", "src", "buildInfo.js");
const coreContent = `// packages/core/src/buildInfo.js
// GENERATED by scripts/generate-build-info.js — do not edit manually.
// This file overwrites the committed fallback. In fresh clones and local dev
// without the generator, the fallback values are used instead.

export const BUILD_INFO = Object.freeze({
  version: ${JSON.stringify(version)},
  git_sha: ${JSON.stringify(gitSha)},
  built_at: ${JSON.stringify(builtAt)},
  source: ${JSON.stringify(source)},
});

export const VERSION = BUILD_INFO.version;
`;

await writeFile(corePath, coreContent, "utf-8");
console.log(`[build-info] wrote ${corePath}`);
